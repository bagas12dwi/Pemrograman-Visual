xPertama = int(input("Masukkan x1 : ")) 
yPertama = int(input("Masukkan y1 : ")) 
xKedua = int(input("Masukkan x2 : ")) 
yKedua = int(input("Masukkan y2 : ")) 

midpointX = (xPertama + xKedua) / 2
midpointY = (yPertama + yKedua) / 2

print("Nilai midpoint x : " + str(midpointX))
print("Nilai midpoint y : " + str(midpointY))
print("Jadi titik tengahnya (midpoint) adalah " + str(midpointX) + ', ' + str(midpointY))

